package com.example.lab1;
/**
 * Function class Max
 */
public class Max {
    /**
     * Function finds the biggest number from:
     * @param a - first param
     * @param b - second param
     * @return the biggest
     */
    static int max(int a, int b)
    {
        if(a >= b)
        {
            return a;
        }
        else
        {
            return b;
        }
    }
}
