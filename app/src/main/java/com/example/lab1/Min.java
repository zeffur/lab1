package com.example.lab1;
/**
 * Function class Min
 */
public class Min {
    /**
     * Function finds the smallest number from:
     * @param a - first param
     * @param b - second param
     * @return the smallest
     */
    static int min(int a, int b)
    {
        if(a <= b)
        {
            return a;
        }
        else
        {
            return b;
        }
    }
}
